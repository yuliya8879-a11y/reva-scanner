    async def generate_full_free_report(
        self, answers: dict, soul_number=None
    ) -> tuple[str, dict]:
        """Полный бесплатный разбор (первый скан). Sonnet."""
        user_name = answers.get("user_name", "")
        birth_date = answers.get("birth_date", "")
        request = answers.get("request", "")

        parts = [f"Имя клиента: {user_name}"]
        if birth_date:
            parts.append(f"Дата рождения: {birth_date}")
        if request:
            parts.append(f"Запрос: {request}")
        parts.append(
            "\nПроведи ПОЛНОЕ сканирование по всем блокам алгоритма. "
            "Каждый блок развёрнуто, конкретно. "
            "Не пиши «недостаточно данных» — ты сканируешь поле, не анкету."
        )
        prompt = "\n".join(parts)

        response = await messages_create(
            model="claude-sonnet-4-6",
            max_tokens=3000,
            system=_MINI_FREE_SYSTEM,
            messages=[{"role": "user", "content": prompt}],
        )
        usage = response.usage
        logger.info("free-scan tokens: in=%s out=%s", usage.input_tokens, usage.output_tokens)
        return response.content[0].text, {"input_tokens": usage.input_tokens, "output_tokens": usage.output_tokens}

